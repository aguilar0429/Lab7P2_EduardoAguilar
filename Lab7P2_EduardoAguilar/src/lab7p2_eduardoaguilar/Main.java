    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        String type="";
        if(luna.isSelected()){
            type = "Luna";
        }else if(planeta.isSelected()){
            type = "Planeta";
        }
        String name = jt_named.getText();
        jt_named.setText("");
        int distance = (int) sp_distanciad.getValue();
        sp_distanciad.setValue(0);
        String superficie="";
        if(rocky.isSelected()){
            superficie = "Rocoso";
        }else if (liquido.isSelected()){
            superficie = "Liquido";
        }else if(gas.isSelected()){
            superficie = "Gaseoso";
        }
        int saturacion = (int)sp_saturacion.getValue();
        sp_saturacion.setValue(0);
        
        Destinos d = new Destinos (type,name,distance,superficie,saturacion);
        AdminDestinos ap = new AdminDestinos("./destinos.ctm");
        ap.cargarArchivo();
        ap.setDestinos(d);
        ap.escribirArchivo();
        JOptionPane.showMessageDialog(this,
                "Destino guardado exitosamente");
    }//GEN-LAST:event_jButton3MouseClicked

