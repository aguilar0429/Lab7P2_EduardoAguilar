    private void jButton6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton6MouseClicked
        
        if(agregar == 0){
            int x = lista2.getSelectedIndex();
            DefaultListModel l = (DefaultListModel) lista2.getModel();
            Object temp = l.get(x);
            consumo = ((Nave) temp).getGasto();
            velocidad = ((Nave) temp).getSpeed();
            maxc = ((Nave)temp).getCombustible();
            tanque2 = ((Nave)temp).getCombustible2();
            
            lista_s.add(temp);
            DefaultListModel a = (DefaultListModel) lista.getModel();
            for (Object n : lista_s) {
                a.addElement(n);
            }
            lista.setModel(a);
            
        }else if(agregar == 1){
            int x = lista2.getSelectedIndex();
            DefaultListModel l = (DefaultListModel) lista2.getModel();
            Object temp = l.get(x);
            
            if(((Astronautas)temp).getTitulo().equalsIgnoreCase("Fisico") || ((Astronautas)temp).getTitulo().equalsIgnoreCase("Ingeniero")
                    || ((Astronautas)temp).getTitulo().equalsIgnoreCase("Astrofisico")){
                contador++;
            }
            
            
            lista_s.add(temp);
            DefaultListModel a = (DefaultListModel) lista.getModel();
            for (Object n : lista_s) {
                a.addElement(n);
            }
            lista.setModel(a);
        }else if (agregar == 2){
            int x = lista2.getSelectedIndex();
            DefaultListModel l = (DefaultListModel) lista2.getModel();
            Object temp = l.get(x);
            lejania = ((Destinos)temp).getDistancia();
            lista_s.add(temp);
            contador2++;
            DefaultListModel a = (DefaultListModel) lista.getModel();
            for (Object n : lista_s) {
                a.addElement(n);
            }
            lista.setModel(a);
        }
        
    }//GEN-LAST:event_jButton6MouseClicked

