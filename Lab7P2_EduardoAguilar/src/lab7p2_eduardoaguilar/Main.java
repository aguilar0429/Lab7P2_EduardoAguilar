    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        
        
        if (contador < 3 || contador2!= 1){
            JOptionPane.showMessageDialog(null,"No se puede empezar la simulacion ya que "
                    + "\nno se cumplen los requisitos");
        }else{
            int recorrido = 0;
            AdminBarra ab = new AdminBarra(consumo, combustiblebar, lejania, maxc);
            Thread barra = new Thread(ab);
            barra.start();
            recorrido = maxc * consumo;
            if (recorrido < lejania) {
                int option = JOptionPane.showConfirmDialog(null, "Desea usar tanque reserva?");
                if (option == 0) {
                    AdminBarra as = new AdminBarra(consumo, barra2, lejania - recorrido, tanque2);
                    Thread barraa = new Thread(ab);
                    barraa.start();
                }
            }
        }
        
            
        
        
    }//GEN-LAST:event_jButton4MouseClicked

